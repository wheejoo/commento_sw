package com.devfun.vo;

public class VirusVO {
	
	private String region;
	private int confirmed_case;
	private int under_treatment;
	private int the_dead;
	private int quarantine_release;
	private float incidence_rate;
	
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public int getConfirmed_case() {
		return confirmed_case;
	}
	public void setConfirmed_case(int confirmed_case) {
		this.confirmed_case = confirmed_case;
	}
	public int getUnder_treatment() {
		return under_treatment;
	}
	public void setUnder_treatment(int under_treatment) {
		this.under_treatment = under_treatment;
	}
	public int getThe_dead() {
		return the_dead;
	}
	public void setThe_dead(int the_dead) {
		this.the_dead = the_dead;
	}
	public int getQuarantine_release() {
		return quarantine_release;
	}
	public void setQuarantine_release(int quarantine_release) {
		this.quarantine_release = quarantine_release;
	}
	public float getIncidence_rate() {
		return incidence_rate;
	}
	public void setIncidence_rate(float incidence_rate) {
		this.incidence_rate = incidence_rate;
	}
	
	


}
